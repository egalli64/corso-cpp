#include <iostream>

int main()
{
    int x = 42;
    int y = 18;

    std::cout << "x is " << x << '\n';
    std::cout << "y is " << y << '\n';

    std::cout << "after ++x the value in x is " << ++x << '\n';
    std::cout << "after --x the value in x is " << --x << '\n';
}
